$(document).ready(function() {
    // Initialize Lightbox
    $('[data-lightbox="gallery"]').lightcase({
        swipe: true,
        showSequenceInfo: false,
        showTitle: true,
        showCaption: true,
        transition: 'elastic'
    });

    // Filter functionality
    $('.filter-btn').on('click', function() {
        const filter = $(this).data('filter');
        
        // Update active button
        $('.filter-btn').removeClass('active');
        $(this).addClass('active');
        
        // Filter items with animation
        if (filter === 'all') {
            $('.gallery-item').removeClass('hidden').fadeIn(400);
        } else {
            $('.gallery-item').addClass('hidden').fadeOut(200);
            setTimeout(function() {
                $(`.gallery-item[data-category="${filter}"]`).removeClass('hidden').fadeIn(400);
            }, 200);
        }
    });

    // Load more functionality
    $('#load-more').on('click', function() {
        const btn = $(this);
        const btnText = btn.find('.btn-text');
        const btnIcon = btn.find('.btn-icon i');
        
        // Show loading state
        btnText.text('Loading...');
        btnIcon.removeClass('fa-plus').addClass('fa-spinner fa-spin');
        btn.prop('disabled', true);
        
        // Simulate loading (replace with actual AJAX call)
        setTimeout(function() {
            btnText.text('Load More Images');
            btnIcon.removeClass('fa-spinner fa-spin').addClass('fa-plus');
            btn.prop('disabled', false);
            
            // Add more images here
            // You can implement AJAX to load more images from server
        }, 2000);
    });

    // Counter animation for stats
    function animateCounter(element, target, duration = 1500) {
        let start = 0;
        const increment = target / (duration / 16);
        
        function updateCounter() {
            start += increment;
            if (start < target) {
                element.text(Math.floor(start));
                requestAnimationFrame(updateCounter);
            } else {
                element.text(target);
            }
        }
        updateCounter();
    }

    // Start counter animation when stats are visible
    const statsObserver = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const statNumbers = entry.target.querySelectorAll('.stat-number');
                statNumbers.forEach(stat => {
                    const target = parseInt(stat.textContent.replace('+', ''));
                    animateCounter(stat, target);
                });
                statsObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });

    // Observe the stats section
    const statsSection = document.querySelector('.gallery-stats');
    if (statsSection) {
        statsObserver.observe(statsSection);
    }

    // Smooth scroll for filter buttons
    $('.filter-btn').on('click', function() {
        $('html, body').animate({
            scrollTop: $('.gallery-container').offset().top - 100
        }, 600);
    });

    // Add intersection observer for animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    // Observe gallery items
    $('.gallery-item').each(function() {
        observer.observe(this);
    });
});
</script>
